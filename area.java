public class Main 
{
    public static void main(String[] args) 
   {
        double radius = 7.0; 
        double area = Math.PI * radius * radius;

        System.out.println("Area of the circle: " + area);

        double base = 10.0;          
        double height = 5.0;  

        double area = (base * height) / 2;

        System.out.println("Area of the triangle: " + area);
    }


    
}
